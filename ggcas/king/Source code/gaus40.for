	SUBROUTINE GAUS40 (FCN,AREA)
c
c       INTEGRA TRA A E B LA FUNZIONE FCN(X) CON IL METODO
c       DI GAUSS-LEGENDRE. SONO USATI 40 PUNTI.
c       I PARAMETRI DA CUI FCN(X) DIPENDE SONO PASSATI NELLA FUNCTION
c       FCN(X) CON IL COMMON/PARFCN/
c
c
	IMPLICIT REAL*8 (A-H,O-Z)
	DIMENSION X(20),W(20)
        COMMON/ESTREMI/A,B
c
	DATA X /.038772417506050821933, .116084070675255208483,
     1          .192697580701371099716, .268152185007253681141,
     2          .341994090825758473007, .413779204371605001525,
     3          .483075801686178712909, .549467125095128202076,
     4          .612553889667980237953, .671956684614179548379,
     5          .727318255189927103281, .778305651426519387695,
     6          .824612230833311663196, .865959503212259503821,
     7          .902098806968874296728, .932812808278676533361,
     8          .957916819213791655805, .977259949983774262663,
     9          .990726238699457006453, .998237709710559200350/
c
       	DATA W /.077505947978424811264, .077039818164247965588,
     1          .076110361900626242372, .074723169057968264200,
     2          .072886582395804059061, .070611647391286779695,
     3          .067912045815233903826, .064804013456601038075,
     4          .061306242492928939167, .057439769099391551367,
     5          .053227846983936824355, .048695807635072232061,
     6          .043870908185673271992, .038782167974472017640,
     7          .033460195282547847393, .027937006980023401098,
     8          .022245849194166957262, .016421058381907888713,
     9          .010498284531152813615, .004521277098533191258/
c
	AREA=0.D0
	DO 1 I=1,20
	XI = (B-A)/2.D0*X(I)+(B+A)/2.0D0
        AREA = AREA + W(I)*FCN(XI)
	XI =-(B-A)/2.D0*X(I)+(B+A)/2.0D0
        AREA = AREA + W(I)*FCN(XI)
 1	CONTINUE
        AREA = (B-A)/2.D0*AREA 
        RETURN
        END

