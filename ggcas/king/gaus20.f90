	SUBROUTINE GAUS20 (FCN,AREA)
!       INTEGRA TRA A E B LA FUNZIONE FCN(X) CON IL METODO
!       DI GAUSS-LEGENDRE. SONO USATI 20 PUNTI.
!       I PARAMETRI DA CUI FCN(X) DIPENDE SONO PASSATI NELLA FUNCTION
!       FCN(X) CON IL COMMON/PARFCN/
!
!
	IMPLICIT REAL*8 (A-H,O-Z)
        COMMON/ESTREMI/A,B
	DIMENSION X(10),W(10)
!
	DATA X /.076526521133497333755, .227785851141645078080,&
               .373706088715419560673, .510867001950827098004,&
               .636053680726515025453, .746331906460150792614,&
               .839116971822218823395, .912234428251325905868,&
               .963971927277913791268, .993128599185094924786/
!
       	DATA W /.152753387130725850698, .149172986472603746788,&
               .142096109318382051329, .131688638449176626898,&
               .118194531961518417312, .101930119817240435037,&
               .083276741576704748725, .062672048334109063570,&
               .040601429800386941331, .017614007139152118312/
!
	AREA=0.D0
	DO 1 I=1,10
	XI = (B-A)/2.D0*X(I)+(B+A)/2.0D0
        AREA = AREA + W(I)*FCN(XI)
	XI =-(B-A)/2.D0*X(I)+(B+A)/2.0D0
        AREA = AREA + W(I)*FCN(XI)
 1	CONTINUE
    AREA = (B-A)/2.D0*AREA 
    RETURN
    END
