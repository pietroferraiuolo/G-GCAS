        AREA=0.D0
        DO 1 I=1,40
        XI = (B-A)/2.D0*X(I)+(B+A)/2.0D0
            AREA = AREA + W(I)*FCN(XI)
        XI =-(B-A)/2.D0*X(I)+(B+A)/2.0D0
            AREA = AREA + W(I)*FCN(XI)
     1	CONTINUE

        AREA = (B-A)/2.D0*AREA 
        RETURN
